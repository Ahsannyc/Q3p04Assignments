def main():
    # Prompt the user with "What's your favorite animal? " (note the space after the question)
    animal = input("Whats your favorite animal? ")  # Get the user's favorite animal

    # Respond the user with "My favorite animal is also [user's animal]!"
    # Using the input directly in the string without any modification
    print("My favorite animal is also " + animal + "!")

    # This provided line is required at the end of the 
    # python file to call the main() function.
if __name__ == '__main__':
    main()
        





